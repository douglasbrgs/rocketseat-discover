// multiplicacao
console.log(3.2 * 5.5);

// divisão
console.log(12 / 2);

// soma
console.log(34 + 67);

// subtração
console.log(34 - 23);

// resto da divisão
let remainder = 11 % 9; // 2
console.log(remainder);

// incremento
let increment = 0;
console.log(increment++); // 0
console.log(increment); // 1

let incremento = 0;
console.log(++incremento); // 1
console.log(incremento); // 1

// decremento
let decrement = 0;
console.log(decrement--); // 0
console.log(decrement); // -1

// exponencial
console.log(2 ** 3); // 8
